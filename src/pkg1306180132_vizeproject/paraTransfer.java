    public int sendingMoneyToOtherPerson(String Ad, String Soyad, String Eposta, String gonderilenMiktar) throws FileNotFoundException, IOException {

        boolean found = false;
        for (int i = 0; i < Database.veriMusteri.size(); i++) {
            // to check wehter the customer is existed or not 
            if (Database.veriMusteri.elementAt(i).Ad.equals(Ad)
                    && Database.veriMusteri.elementAt(i).Soyad.equals(Soyad)
                    && Database.veriMusteri.elementAt(i).Eposta.equals(Eposta)) {

                double result = Double.parseDouble(EntranceOfMusteri.Bakiye);
                double output = result - Double.parseDouble(gonderilenMiktar);
                if (result > 0 && output > 0) {
                    found = true;
                    String eskiBakiye = Database.veriMusteri.elementAt(i).bakiye;
                    String newBakiye = Double.toString(Double.parseDouble(eskiBakiye)
                            + Double.parseDouble(gonderilenMiktar));
                    // here is the adding of money to new account 
                    Database.veriMusteri.elementAt(i).bakiye = newBakiye;

                    JOptionPane.showMessageDialog(null, "Done Successfully\n");

                    JOptionPane.showMessageDialog(null, "Alici kisi     " + Ad + "  " + Soyad
                            + "\n  Eski Bakiye = " + eskiBakiye
                            + "\n  YeniBakiye   =  " + newBakiye,
                             "OUTPUT", JOptionPane.PLAIN_MESSAGE);
                } else {
                    return 2;
                }

            }
        }

        // here to modify the data of sending person ( to subtract data from the sent person)
        if (found) {
            for (int i = 0; i < Database.veriMusteri.size(); i++) {
                if ((Database.veriMusteri.elementAt(i).Ad).equals(EntranceOfMusteri.Ad)
                        && (Database.veriMusteri.elementAt(i).Soyad).equals(EntranceOfMusteri.Soyad)
                        && (Database.veriMusteri.elementAt(i).Sifre).equals(EntranceOfMusteri.Sifre)) {
                    double result = Double.parseDouble(Database.veriMusteri.elementAt(i).bakiye);
                    Database.veriMusteri.elementAt(i).bakiye = Double.toString(result - Double.parseDouble(gonderilenMiktar));

                    JOptionPane.showMessageDialog(null, "Gonderen kisi     " + EntranceOfMusteri.Ad + "  "
                            + "\n    Eski Bakiye =" + result
                            + "\n    Yeni Bakiye =" + Database.veriMusteri.elementAt(i).bakiye);
                    return 1;
                }

            }
        }
        return 0;
    }

